bool Frame::CheckEpiLineDistToRmDynamicPoint(const cv::KeyPoint &kp1,const cv::Point2f &kp2, cv::Mat &F12
                                             ,const float threshold)
{
    // Epipolar line in second image l = x1'F12 = [a b c]
    double a = kp1.pt.x*F12.at<double>(0,0)+kp1.pt.y*F12.at<double>(0,1)+F12.at<double>(0,2);
    double b = kp1.pt.x*F12.at<double>(1,0)+kp1.pt.y*F12.at<double>(1,1)+F12.at<double>(1,2);
    double c = kp1.pt.x*F12.at<double>(2,0)+kp1.pt.y*F12.at<double>(2,1)+F12.at<double>(2,2);
    double son = fabs(a*kp2.x + b*kp2.y + c);
    double mom = sqrt(a*a + b*b);
    double dist = son / mom;
    return dist < threshold;
}

float Frame::getlimitepi(const cv::KeyPoint &kp1,const cv::Mat &imDepth, const float D1,  const float D2,const cv::Mat &imMask )
{
	int x = kp1.pt.x;
	int y = kp1.pt.y;
	float depth_value = imDepth.at<float>(y, x);
	float limit = ((D1 - depth_value)/(D1 - D2)) * D1;
 	int val = (int)imMask.at<uchar>(y,x)/255;
        if (val == 0)
		return limit*0.02;
	else
		return limit*0.1;
}

